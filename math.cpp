#include<bits/stdc++.h>
using namespace std;

int main () {
    cout << "sqrt(4) = " << sqrt(4) << endl;
    cout << "pow(2, 3) = " << pow(2, 3) << endl;
    cout << "abs(-4.1) = " << abs(-4.1) << endl;
    cout << "ceil(4.1) = " << ceil(4.1) << endl;
    cout << "floor(4.1) = " << floor(4.1) << endl;
}